package com.java;

public class Divisible5And3 {
  public static void main(String args[]) {
	  int num = 121;
	  if(num % 5==0 && num % 3==0) {
		  System.out.println("The number is divisible by 5 and 3");
	  }
	  else {
		  System.out.println("The number is not divisible by 5 and 3");
	  }
  }
}
